export const defaultWeights: PreferenceWeights = {
  cleanliness: 85,
  food: 70,
  kidsFriendly: 60,
  petFriendly: 30,
  fitness: 55,
  nearbyAttractions: 75,
  safety: 80,
  valueForMoney: 78,
  quietPrivacy: 68,
  serviceQuality: 82,
};

const metricScale: PreferenceKey[] = [
  "cleanliness",
  "food",
  "kidsFriendly",
  "petFriendly",
  "fitness",
  "nearbyAttractions",
  "safety",
  "valueForMoney",
  "quietPrivacy",
  "serviceQuality",
];

export function computeMatchScore(
  hotel: Hotel,
  weights: PreferenceWeights,
): number {
  const totalWeight = metricScale.reduce((sum, key) => sum + weights[key], 0);
  if (totalWeight <= 0) return 0;

  const weighted = metricScale.reduce((sum, key) => {
    const scoreOutOf100 = (hotel.metrics[key] / 5) * 100;
    return sum + scoreOutOf100 * weights[key];
  }, 0);

  return Math.round(weighted / totalWeight);
}
